export function findQuestionsByDiagnosis(diagnosis: string) {
  return QUESTIONS.find(question => question.Diagnosis === diagnosis);
}
export function findSections(diagnosis: string) {
  let sections: string [];
  sections = [];
  for (const ques of QUESTIONS) {
    if (ques.Diagnosis === diagnosis && !sections.includes(ques.Section)) {
      sections.push(ques.Section);
    }
  }
  return sections;
}
export function findQuesForSec(diagnosis: string, section: string) {
  let questions: any [];
  questions = [];
  for (const ques of QUESTIONS) {
    if (ques.Diagnosis === diagnosis && ques.Section === section) {
      questions.push(ques);
    }
  }
  return questions;
}

export function getRelatedQuestionsByQuestion(question: string) {
  let q;
  for (const ques of QUESTIONS) {
    if (ques.Question === question) {
      q = ques;
    }
  }
  let listRelated;
  listRelated = [];
  q.Related_QS.map(n => {
    for (const ques of QUESTIONS) {
      if (ques.ID === n) {
        listRelated.push(ques.Question);
      }
    }
  });
  return listRelated;
}
export function getQuestionByQuestion(question: string) {
  let q;
  for (const ques of QUESTIONS) {
    if (ques.Question === question) {
      q = ques;
    }
  }
  return q;
}
export function getQuestionByID(id: number) {
  for (const ques of QUESTIONS) {
    if (ques.ID === id) {
      return ques;
    }
  }
}
